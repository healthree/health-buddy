const router = require("express").Router();
const { Clients } = require("../../models");

router.get("/", (req, res) => {
  Clients.findAll()
    .then((dbCommentData) => res.json(dbCommentData))
    .catch((err) => {
      console.log(err);
      res.status(500).json(err);
    });
});

router.get("/:id", (req, res) => {
  Clients.findOne({
    where: {
      id: req.params.id,
    },
  })
    .then((dbPostData) => {
      if (!dbPostData) {
        res.status(404).json({ message: "No cilent found with this id" });
        return;
      }
      res.json(dbPostData);
    })
    .catch((err) => {
      console.log(err);
      res.status(500).json(err);
    });
});

router.post("/", (req, res) => {
  Clients.create({
    name: req.body.name,
    date_of_birth: req.body.date_of_birth,
    date_of_enrollment: req.body.date_of_enrollment,
    symptoms: req.body.symptoms,
    medical_history: req.body.medical_history,
    doctors_id: req.body.doctors_id,
  })
    .then((dbPostData) => res.json(dbPostData))
    .catch((err) => {
      console.log(err);
      res.status(500).json(err);
    });
});

router.put("/:id", (req, res) => {
  Clients.update(
    {
      name: req.body.name,
      date_of_birth: req.body.date_of_birth,
      date_of_enrollment: req.body.date_of_enrollment,
      symptoms: req.body.symptoms,
      medical_history: req.body.medical_history,
      doctors_id: req.body.doctors_id,
    },
    {
      where: {
        id: req.params.id,
      },
    }
  )
    .then((dbPostData) => {
      if (!dbPostData) {
        res.status(404).json({ message: "No client found with this id" });
        return;
      }
      res.json(dbPostData);
    })
    .catch((err) => {
      console.log(err);
      res.status(500).json(err);
    });
});

router.delete("/:id", (req, res) => {
  Clients.destroy({
    where: {
      id: req.params.id,
    },
  })
    .then((dbPostData) => {
      if (!dbPostData) {
        res.status(404).json({ message: "No client found with this id" });
        return;
      }
      res.json(dbPostData);
    })
    .catch((err) => {
      console.log(err);
      res.status(500).json(err);
    });
});

module.exports = router;
